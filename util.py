
def kts_to_mps(kts):
    """
    knots to meters per second
    """
    return kts * 0.514444

def mps_to_kts(mps):
    """
    meters per second to knots
    """
    return mps * 1.94384

def mps_to_fpm(mps):
    """
    meters per second to feet per minute
    """
    return mps * 196.85

def fpm_to_mps(fpm):
    """
    feet per minute to meters per second
    """
    return fpm / 196.85

def me_to_ft(me):
    """
    meters to feets
    """
    return me * 3.28084

def ft_to_me(ft):
    """
    feets to meters
    """
    return ft * 0.3048